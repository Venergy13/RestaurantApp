class ListActivity : ComponentActivity() {
    @OptIn(ExperimentalMaterial3Api::class)
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

        val dishTypeString = intent.getStringExtra("dishType")
        val dishType = try {
            DishType.valueOf(dishTypeString ?: "")
        } catch (e: IllegalArgumentException) {
            null
        }

        if (dishType != null) {
            setContent {
                CenterAlignedTopAppBarExample(dishType)
            }
        } else {
            setContent {
                CenterAlignedTopAppBarExample(dishType = DishType.STARTER)
            }
        }
    }

    override fun onPause() {
        super.onPause()
        Log.d("ListActivity", "onPause")
    }

    override fun onResume() {
        super.onResume()
        Log.d("ListActivity", "onResume")
    }

    override fun onDestroy() {
        Log.d("ListActivity", "onDestroy")
        super.onDestroy()
    }
}




